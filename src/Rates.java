public class Rates {
    private float USD;
    private float EUR;
    private float BRL;
    private float ARS;
    private float BOB;
    private float CLP;
    private float COP;


    public float getUSD() {
        return USD;
    }

    public void setUSD(float USD) {
        this.USD = USD;
    }

    public float getEUR() {
        return EUR;
    }

    public void setEUR(float EUR) {
        this.EUR = EUR;
    }

    public float getBRL() {
        return BRL;
    }

    public void setBRL(float BRL) {
        this.BRL = BRL;
    }

    public float getARS() {
        return ARS;
    }

    public void setARS(float ARS) {
        this.ARS = ARS;
    }

    public float getBOB() {
        return BOB;
    }

    public void setBOB(float BOB) {
        this.BOB = BOB;
    }

    public float getCLP() {
        return CLP;
    }

    public void setCLP(float CLP) {
        this.CLP = CLP;
    }

    public float getCOP() {
        return COP;
    }

    public void setCOP(float COP) {
        this.COP = COP;
    }

}